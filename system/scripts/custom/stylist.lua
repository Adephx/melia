local getoptions
local gethairtype
local getnextstyle
local getprevstyle
local findstyle
local getnextcolor
local getprevcolor

function npc_stylist()
	local pc = getpc()
	local style = pc.hair
	
	local selection = nselect("What can I do for you today?", "Change Hair Style:hair", "Change Hair Color:color", "Nothing:end")
	if selection == "end" then
		msg("Please come back any time.")
		return
	end

	local changetype = 1
	if selection == "color" then
		changetype = 2
	end
	
	msg("Good decision, one should treat himself once in a while.")
	
	local direction = 1
	while true do
		local options = getoptions(direction)
		local hairtype = gethairtype(pc.gender, style)
		
		changehair(style)
		selection = nselect("This is the style \"" .. hairtype.name .. "\"  (#" .. style .. "), what do you think?", options)

		if selection == "n" then
			if changetype == 1 then
				style = getnextstyle(pc.gender, hairtype)
			else
				style = getnextcolor(pc.gender, hairtype)
			end
			direction = 1
		elseif selection == "p" then
			if changetype == 1 then
				style = getprevstyle(pc.gender, hairtype)
			else
				style = getprevcolor(pc.gender, hairtype)
			end
			direction = 2
		elseif selection == "j" then
			local jumpStyleStr = input("Which style would you like to see?")
			
			local jumpStyle = hairtype.index
			local hairtype = nil
			
			local match = jumpStyleStr:match("^[0-9]+$")
			if match ~= nil then
				jumpStyle = tonumber(jumpStyleStr)
				hairtype = gethairtype(pc.gender, jumpStyle)
			end
			
			if hairtype == nil then
				msg("Sorry, I don't know that style.")
			else
				style = hairtype.index
			end
		else
			msg("Yes, this style does suit you well. Please come back any time.")
			return
		end
	end
end

