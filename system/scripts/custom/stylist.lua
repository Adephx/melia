function getoptions(direction)
	local options = {}

	for i=1,2 do
		if direction == i then
			table.insert(options, "Next:n")
		else
			table.insert(options, "Prev:p")
		end
	end
	
	table.insert(options, "Jump:j")
	table.insert(options, "I like it")

	return options
end

function gethairtype(gender, indexOrName)
	for _, v in pairs(hairdata.types) do
		if v.gender == gender and (v.index == indexOrName or v.className == indexOrName) then
			return v
		end
	end

	return nil
end

function getnextstyle(gender, curType)
	local found = false
	local first = nil
	
	local list = gender == GENDER_MALE and hairdata.malestyles or hairdata.femalestyles
	
	-- Loop through the styles to find the current one, then continue
	-- once more to get to the next one. If we end up leaving the loop,
	-- because the current one was the last one, we fall back to the
	-- first value in the list.
	for k,className in ipairs(list) do
		if found then
			local hairtype = gethairtype(gender, className)
			return hairtype.index
		end
		if className == curType.className then
			found = true
		end
		if first == nil then
			first = className
		end
	end
	
	local hairtype = gethairtype(gender, first)
	return hairtype.index
end

function getprevstyle(gender, curType)
	local prev = nil
	
	local list = gender == GENDER_MALE and hairdata.malestyles or hairdata.femalestyles
	
	-- Loop through the styles to find the current one. If we find it,
	-- and a prev value was set, we leave the loop to return that style.
	-- If we find the current one before a previous one was set, the
	-- current one is the first one, and we return the last item.
	for k,className in ipairs(list) do
		if className == curType.className then
			if prev == nil then
				prev = list[#list]
			end
			
			break
		end
		
		prev = className
	end
	
	local hairtype = gethairtype(gender, prev)
	return hairtype.index
end

