function getnextcolor(gender, curType)
	local result = nil
	
	for _,hairtype in pairs(hairdata.types) do
		if hairtype.gender == gender and hairtype.className == curType.className then
			if hairtype.index > curType.index then
				result = hairtype.index
				break
			end
			
			if result == nil then
				result = hairtype.index
			end
		end
	end

	return result
end

function getprevcolor(gender, curType)
	local result = nil
	local prev = nil
	
	for _,hairtype in pairs(hairdata.types) do
		if hairtype.gender == gender and hairtype.className == curType.className then
			if hairtype.index == curType.index then
				if prev ~= nil then
					result = prev
					break
				end
			end
			
			prev = hairtype.index
		end
	end
	
	if result == nil then
		result = prev
	end

	return result
end
